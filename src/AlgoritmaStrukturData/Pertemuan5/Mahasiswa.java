package AlgoritmaStrukturData.Pertemuan5;

public class Mahasiswa {
    String nama,nim;
    int tahunMasuk;
    double nilaiUts,nilaiUas;

    public Mahasiswa(String nama, String nim, int tahunMasuk, double nilaiUts, double nilaiUas) {
        this.nama = nama;
        this.nim = nim;
        this.tahunMasuk = tahunMasuk;
        this.nilaiUts = nilaiUts;
        this.nilaiUas = nilaiUas;
    }
}
